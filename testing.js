// Testing adalah proses untuk memastikan bahwa aplikasi web berfungsi dengan baik dan aman dari serangan.
// Testing dapat dilakukan dengan cara melakukan pengujian fungsional, pengujian keamanan, dan pengujian performa.

// Unit Testing
// Unit testing adalah teknik pengujian perangkat lunak yang dilakukan pada unit terkecil dari kode sumber, yaitu fungsi atau metode.

// Unit testing bertujuan untuk memastikan bahwa setiap unit kode berfungsi dengan baik dan sesuai dengan spesifikasi yang ditentukan.

// Nextjs jest adalah framework pengujian yang digunakan untuk melakukan unit testing pada aplikasi Next.js.
// Jest adalah framework pengujian yang dikembangkan oleh Facebook dan digunakan untuk melakukan pengujian pada aplikasi JavaScript, termasuk aplikasi Next.js.

// render adalah fungsi yang digunakan untuk merender komponen React pada pengujian.
// Render digunakan untuk membuat salinan dari komponen React yang akan diuji, sehingga kita dapat memeriksa hasil rendernya.

// screen adalah fungsi yang digunakan untuk mengambil elemen dari DOM berdasarkan selektor CSS.

// assertion adalah pernyataan yang digunakan untuk memeriksa apakah hasil dari pengujian sesuai dengan yang diharapkan.
// Assertion digunakan untuk membandingkan hasil yang diharapkan dengan hasil yang sebenarnya dari pengujian.

// expect adalah fungsi yang digunakan untuk membuat pernyataan pengujian.

// mock adalah teknik yang digunakan untuk menggantikan objek atau fungsi yang sebenarnya dengan objek atau fungsi tiruan selama pengujian.
// Mock digunakan untuk mengisolasi unit yang sedang diuji dari dependensi eksternal, sehingga pengujian dapat dilakukan dengan lebih mudah dan cepat.

// fireEvent adalah fungsi yang digunakan untuk mensimulasikan peristiwa (event) pada elemen DOM selama pengujian.

// modulePaths adalah konfigurasi yang digunakan untuk menentukan jalur modul yang akan digunakan dalam pengujian.

// beforeEach adalah fungsi yang digunakan untuk mengeksekusi kode sebelum setiap pengujian dijalankan.
// AfterEach adalah fungsi yang digunakan untuk mengeksekusi kode setelah setiap pengujian dijalankan.
// beforeAll adalah fungsi yang digunakan untuk mengeksekusi kode sebelum semua pengujian dijalankan.
// afterAll adalah fungsi yang digunakan untuk mengeksekusi kode setelah semua pengujian dijalankan.